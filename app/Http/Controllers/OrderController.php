    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //$invoice_code = "INV_" . date('YmdHis') .  $request->user_id . $request->product_id;
        $primary_address = Address::where("user_id", Auth::user()->id)->where("is_primary", "1")->first();

        Order::create([
            'user_id' => $request->user_id,
            'product_id' => $request->product_id,
            'status_id' => $request->status_id,
            'quantity' => $request->quantity,
            'address_id' => $primary_address->id,
            // 'invoice_code' => $invoice_code
        ]);

        return redirect()->back()->with('status', 'Add to Cart success!');
    }

